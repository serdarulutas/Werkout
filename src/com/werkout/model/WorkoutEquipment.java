package com.werkout.model;



public class WorkoutEquipment {
	private String id;
	private String name;
	private Media image;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Media getImage() {
		return image;
	}
	public void setImage(Media image) {
		this.image = image;
	}
	
}
